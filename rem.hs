h2. Servizio non ancora disponibile

Il servizio proposto fornisce due livelli di supporto, uno di scambio puro detto *base* , con una memoria limitata nel tempo e senza conoscenza dello stato del gruppo, l'altro , detto *completo* si prende completamente il lavoro di sincronizzazione, lasciando al gruppo solo la produzione di eventi di responsabile.

Il primo ha un costo di 10 euro annui e prevede che la sincronizzazione venga effettuata da un elemento del gruppo, il secondo costa 15, e mantiene lo stato completo del gruppo.

In entrambi i casi il gruppo deve eseguire la parte del manuale operativo che riguarda la produzione di chiavi responsabile.
Nel caso base deve proseguire fino ad avere il file con estensione _gruppo_ pronta, che deve essere spedita in allegato a gruppobase@reactivegas.org
Nel caso avanzato si deve preparare una cartella contenente le chiavi pubbliche dei responsabili di partenza e spedirla a gruppocompleto@reactivegas.org, il servizio risponderá fornendo una cartella con 2 files, _sincronizzatore.publ_ e _stato_ che devono essere distribuiti ai responsabili. 


bq. Se entro 6 mesi non viene ricevuto il pagamento , il servizio viene interrotto.

